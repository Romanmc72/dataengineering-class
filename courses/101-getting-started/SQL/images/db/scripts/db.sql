/* Create all of the tables... */
CREATE TABLE vendors (
    id                SERIAL PRIMARY KEY,
    name              TEXT UNIQUE,
    primary_email     TEXT,
    created_at        TIMESTAMP NOT NULL,
    last_modified_at  TIMESTAMP NOT NULL
)
;

CREATE TABLE customers (
    id                SERIAL PRIMARY KEY,
    first_name        TEXT,
    last_name         TEXT,
    email             TEXT,
    username          TEXT UNIQUE,
    created_at        TIMESTAMP NOT NULL,
    last_modified_at  TIMESTAMP NOT NULL
)
;

CREATE TABLE employees (
    id                SERIAL PRIMARY KEY,
    system_tag        TEXT UNIQUE,
    first_name        TEXT,
    last_name         TEXT,
    email             TEXT,
    created_at        TIMESTAMP NOT NULL,
    last_modified_at  TIMESTAMP NOT NULL
)
;

CREATE TABLE categories (
    id                SERIAL PRIMARY KEY,
    category_mgr_id   INT REFERENCES employees,
    description       TEXT NOT NULL,
    created_at        TIMESTAMP NOT NULL,
    last_modified_at  TIMESTAMP NOT NULL
)
;

CREATE TABLE products (
    id                SERIAL PRIMARY KEY,
    vendor_id         INT REFERENCES vendors,
    category_id       INT REFERENCES categories,
    cost              NUMERIC(39,2) NOT NULL CHECK (cost > 0),
    minimum_order_qty BIGINT NOT NULL CHECK (minimum_order_qty >= 1),
    description       TEXT NOT NULL,
    last_modified_at  TIMESTAMP NOT NULL,
    created_at        TIMESTAMP NOT NULL
)
;

CREATE TABLE sales (
    id           SERIAL PRIMARY KEY,
    units        BIGINT NOT NULL CHECK (units > 0),
    price        NUMERIC(39,2) NOT NULL CHECK (price >= 0),
    date_of_sale TIMESTAMP NOT NULL,
    state_sold   CHAR(2) NOT NULL,
    product_id   INT REFERENCES products,
    customer_id  INT REFERENCES customers
)
;
